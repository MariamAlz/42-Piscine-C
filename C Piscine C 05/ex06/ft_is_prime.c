int	ft_is_prime(int nb)
{
	int i;
	int j;

	i = nb / 2;
	j = 2;
	if (nb <= 1)
		return (0);
	while (j <= i)
	{
		if (nb % j == 0)
		{
			return (0);
		}
		j++;
	}
	return (1);
}
